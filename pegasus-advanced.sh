performance_benchmark() {
    echo -e "${CYAN}╔════════════════════════════════════════════════════════════════╗${NC}"
    echo -e "${CYAN}║        PEGASUS PERFORMANCE BENCHMARK SUITE                     ║${NC}"
    echo -e "${CYAN}╚════════════════════════════════════════════════════════════════╝${NC}"
    echo ""
    
    # CPU Test
    echo -e "${YELLOW}[1/4] CPU Benchmark...${NC}"
    local cpu_result=$(
        local count=0
        for i in {1..100000}; do
            count=$((count + i))
        done
        echo $count
    )
    echo -e "${GREEN}✓ CPU Test Completed: $cpu_result operations${NC}"
    echo ""
    
    # Bellek Test
    echo -e "${YELLOW}[2/4] Memory Benchmark...${NC}"
    local mem_test=$(dd if=/dev/zero bs=1M count=10 2>&1 | tail -1)
    echo -e "${GREEN}✓ Memory Test: $mem_test${NC}"
    echo ""
    
    # Disk Test
    echo -e "${YELLOW}[3/4] Disk I/O Benchmark...${NC}"
    local disk_test=$(dd if=/dev/zero of=/tmp/test_file bs=1M count=5 2>&1 | tail -1)
    rm -f /tmp/test_file
    echo -e "${GREEN}✓ Disk Test: $disk_test${NC}"
    echo ""
    
    # Network Test (eğer ping mevcut ise)
    echo -e "${YELLOW}[4/4] Network Benchmark...${NC}"
    if command -v ping &> /dev/null; then
        local net_result=$(ping -c 1 8.8.8.8 2>/dev/null | grep time= | tail -1)
        echo -e "${GREEN}✓ Network Test: $net_result${NC}"
    else
        echo -e "${YELLOW}⚠ Network test skipped (ping unavailable)${NC}"
    fi
    echo ""
    
    echo -e "${GREEN}Benchmark tamamlandı!${NC}"
}


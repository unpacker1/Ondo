advanced_system_scan() {
    local scan_report="/sdcard/pegasus_scan_$(date +%Y%m%d_%H%M%S).txt"
    
    {
        echo "╔════════════════════════════════════════════════════════════════╗"
        echo "║           PEGASUS ADVANCED SYSTEM SCAN REPORT                 ║"
        echo "║           $(date '+%Y-%m-%d %H:%M:%S UTC')                                      ║"
        echo "╚════════════════════════════════════════════════════════════════╝"
        echo ""
        
        # CPU Analizi
        echo "[CPU ANALYSIS]"
        if [ -f /proc/cpuinfo ]; then
            echo "Total Cores: $(grep -c "processor" /proc/cpuinfo)"
            echo "Model: $(grep "model name" /proc/cpuinfo | head -1 | cut -d':' -f2)"
            echo "CPU Speed: $(grep "cpu MHz" /proc/cpuinfo | head -1 | cut -d':' -f2)"
        fi
        echo ""
        
        # Bellek Detayları
        echo "[MEMORY ANALYSIS]"
        if [ -f /proc/meminfo ]; then
            awk '
                /MemTotal/ {printf "Total Memory: %d MB\n", $2/1024}
                /MemFree/ {printf "Free Memory: %d MB\n", $2/1024}
                /MemAvailable/ {printf "Available Memory: %d MB\n", $2/1024}
                /Buffers/ {printf "Buffers: %d MB\n", $2/1024}
                /Cached/ {printf "Cached: %d MB\n", $2/1024}
                /SwapTotal/ {printf "Swap Total: %d MB\n", $2/1024}
                /SwapFree/ {printf "Swap Free: %d MB\n", $2/1024}
            ' /proc/meminfo
        fi
        echo ""
        
        # Disk I/O İstatistikleri
        echo "[DISK I/O STATISTICS]"
        if [ -f /proc/diskstats ]; then
            echo "Disk Activity:"
            head -5 /proc/diskstats | awk '{printf "  %s: reads=%s, writes=%s\n", $3, $4, $8}'
        fi
        echo ""
        
        # Ağ İstatistikleri
        echo "[NETWORK STATISTICS]"
        if [ -f /proc/net/dev ]; then
            echo "Interface Statistics:"
            tail -n +3 /proc/net/dev | awk '{printf "  %s: RX=%s bytes, TX=%s bytes\n", $1, $2, $10}'
        fi
        echo ""
        
        # Sistem Yüklenmesi
        echo "[SYSTEM LOAD AVERAGE]"
        if [ -f /proc/loadavg ]; then
            cat /proc/loadavg | awk '{printf "1 min: %s | 5 min: %s | 15 min: %s\n", $1, $2, $3}'
        fi
        echo ""
        
        # Çalışan Hizmetler
        echo "[RUNNING SERVICES]"
        if command -v ps &> /dev/null; then
            ps aux | tail -10 | awk '{printf "  %s (PID: %d, CPU: %s%%)\n", $11, $2, $3}'
        fi
        echo ""
        
        # Açık Dosya Tanıtıcıları
        echo "[OPEN FILE DESCRIPTORS]"
        if [ -d /proc/self/fd ]; then
            echo "Total FDs: $(ls /proc/self/fd | wc -l)"
        fi
        echo ""
        
        echo "═══════════════════════════════════════════════════════════════"
        echo "Report generated by PEGASUS v6.0 - $(hostname)"
        
    } | tee "$scan_report"
    
    echo -e "${GREEN}✓ Tarama raporu kaydedildi: $scan_report${NC}"
}


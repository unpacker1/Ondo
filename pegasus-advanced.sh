network_security_audit() {
    local audit_report="/sdcard/pegasus_security_$(date +%Y%m%d_%H%M%S).txt"
    
    {
        echo "╔════════════════════════════════════════════════════════════════╗"
        echo "║         PEGASUS NETWORK SECURITY AUDIT REPORT                 ║"
        echo "║           $(date '+%Y-%m-%d %H:%M:%S UTC')                                      ║"
        echo "╚════════════════════════════════════════════════════════════════╝"
        echo ""
        
        echo "[FIREWALL STATUS]"
        if command -v iptables &> /dev/null; then
            iptables -L -n | head -20
        else
            echo "iptables not available"
        fi
        echo ""
        
        echo "[LISTENING PORTS]"
        if [ -f /proc/net/tcp ]; then
            echo "TCP Listening Ports:"
            awk '
                NR>1 {
                    if ($4 == "0A") {
                        split($2, a, ":");
                        port = strtonum("0x" a[2]);
                        if (port > 0) printf "  Port: %d (LISTEN)\n", port;
                    }
                }
            ' /proc/net/tcp | sort -u | head -20
        fi
        echo ""
        
        echo "[ESTABLISHED CONNECTIONS]"
        if [ -f /proc/net/tcp ]; then
            count=$(awk 'NR>1 && $4 == "01" {count++} END {print count}' /proc/net/tcp)
            echo "Active Connections: $count"
        fi
        echo ""
        
        echo "[SUSPICIOUS PROCESSES]"
        if command -v ps &> /dev/null; then
            echo "High resource consumers:"
            ps aux --sort=-%cpu | head -6 | tail -5 | awk '{printf "  %s (CPU: %s%%, MEM: %s%%)\n", substr($11,1,30), $3, $4}'
        fi
        echo ""
        
        echo "[SYSTEM SECURITY CHECKS]"
        echo "File Integrity:"
        if [ -f /etc/passwd ]; then
            echo "  /etc/passwd: $(stat /etc/passwd | grep Modify)"
        fi
        if [ -f /etc/shadow ]; then
            echo "  /etc/shadow: $(stat /etc/shadow | grep Modify)"
        fi
        echo ""
        
        echo "═══════════════════════════════════════════════════════════════"
        echo "Security audit generated by PEGASUS v6.0"
        
    } | tee "$audit_report"
    
    echo -e "${GREEN}✓ Güvenlik denetim raporu kaydedildi: $audit_report${NC}"
}


    local json_file="$dir/pegasus_export_$(date +%Y%m%d_%H%M%S).json"
    
    cat > "$json_file" << 'EOF'
{
  "metadata": {
    "version": "6.0",
    "build": "zd404",
    "timestamp": "$(date -Iseconds)",
    "hostname": "$(hostname)"
  },
  "system": {
    "cpu": {
      "usage": "$(get_cpu_info)%",
      "cores": "$(grep -c processor /proc/cpuinfo)"
    },
    "memory": {
      "total_mb": "$(awk '/MemTotal/ {print int($2/1024)}' /proc/meminfo)",
